"use client";

import { GoogleAuthProvider, signInWithPopup } from "firebase/auth";
import { useRouter } from "next/navigation";
import { useState } from "react";
import { useAuth } from "@/lib/firebase/auth-context";
import { getFirebaseAuth } from "@/lib/firebase/config";

export default function SignInPage() {
	const router = useRouter();
	const { user, loading } = useAuth();
	const [error, setError] = useState("");
	const [isSigningIn, setIsSigningIn] = useState(false);

	if (loading) {
		return (
			<div className="flex min-h-screen items-center justify-center">
				<div className="animate-pulse text-gray-400">Loading...</div>
			</div>
		);
	}

	if (user) {
		router.push("/");
		return null;
	}

	async function handleGoogleSignIn() {
		setIsSigningIn(true);
		setError("");

		try {
			const auth = getFirebaseAuth();
			const provider = new GoogleAuthProvider();
			await signInWithPopup(auth, provider);
			router.push("/");
		} catch (err) {
			setError(err instanceof Error ? err.message : "Sign in failed");
			setIsSigningIn(false);
		}
	}

	return (
		<div className="flex min-h-screen items-center justify-center px-4 py-12 sm:px-6 lg:px-8">
			<div className="w-full max-w-md">
				<div className="text-center mb-8">
					<h1 className="text-3xl font-bold tracking-tight text-gray-900">FRD Generator</h1>
					<p className="mt-3 text-base text-gray-600">
						Sign in to generate Functional Requirements Documents
					</p>
				</div>

				<div className="rounded-xl border border-gray-200 bg-white p-6 shadow-sm sm:p-8">
					{error && (
						<div role="alert" className="mb-4 rounded-lg bg-red-50 p-3 text-sm text-red-700">
							{error}
						</div>
					)}

					<button
						type="button"
						onClick={handleGoogleSignIn}
						disabled={isSigningIn}
						className="w-full inline-flex items-center justify-center gap-3 rounded-lg border border-gray-300 bg-white px-6 py-3 text-sm font-medium text-gray-700 transition-colors hover:bg-gray-50 focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-primary focus-visible:ring-offset-2 disabled:cursor-not-allowed disabled:opacity-50"
					>
						<svg className="h-5 w-5" viewBox="0 0 24 24" aria-hidden="true">
							<path
								d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92a5.06 5.06 0 01-2.2 3.32v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.1z"
								fill="#4285F4"
							/>
							<path
								d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
								fill="#34A853"
							/>
							<path
								d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
								fill="#FBBC05"
							/>
							<path
								d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
								fill="#EA4335"
							/>
						</svg>
						{isSigningIn ? "Signing in..." : "Sign in with Google"}
					</button>
				</div>
			</div>
		</div>
	);
}
